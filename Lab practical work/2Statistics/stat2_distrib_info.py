import numpy as np

arr = np.array( [0, 1, 2, 3, 4, 5])

print('original array= ',arr)
print("sum=",np.sum(arr))
print("mean=",np.mean(arr))
print("variance=",np.var(arr))
print("standard deviation=",np.std(arr))
print("sqrt=",np.sqrt(arr))